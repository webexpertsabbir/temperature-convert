function great(number){
    if(number <= 39 && number >= 33 ){
        console.log('d')
    }
    else if(number >= 40 && number <= 49 ){
        console.log('c')
    }
    else if(number >= 50 && number <= 59 ){
        console.log('b')
    }
    else if(number >= 60 && number <= 69 ){
        console.log('A')
    }
    else if(number >= 70 && number <= 100 ){
        console.log('A+')
    }

    else{
        console.log('f')
    }
    
    

}
great(100);
